#include <iostream>

using namespace std;

int main()
{
    int age = 19;
    int bro_age = 29;

    cout << "I am " << age << " years old." << endl;
    cout << "My brother is " << bro_age << " years old." << endl;
    cout << "The difference between our ages is " << bro_age - age << " years." << endl;
    return 0;
} // Output: I am 19 years old.